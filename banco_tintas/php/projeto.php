                <h1>Sobre o Projeto Banco de Tintas</h1>
                <p>O Banco de Tintas é uma iniciativa inovadora que visa coletar doações de latas de tinta parcialmente utilizadas e redistribuí-las para pessoas ou instituições que necessitam. O projeto tem como objetivo minimizar o desperdício e promover a sustentabilidade, garantindo que materiais que poderiam ser descartados indevidamente sejam reutilizados.</p>

                <h2>Prêmios e Reconhecimentos</h2>
                <ul>
                    <li>Prêmio Inovação e Sustentabilidade 2022</li>
                    <li>Reconhecimento pela Prefeitura de Jundiaí como projeto de impacto social</li>
                    <li>Menção honrosa no Fórum de Sustentabilidade 2023</li>
                </ul>
                <p>Desde sua criação, o Banco de Tintas já ajudou dezenas de projetos sociais e centenas de famílias, garantindo acesso a materiais essenciais para reformas e manutenção de espaços comunitários.</p>
